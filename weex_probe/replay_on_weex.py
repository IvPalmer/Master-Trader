def aggregate(results: list, key_prefix: str) -> dict:
    """Compute summary stats for either 'weex_' or 'apples_binance_' results."""
    pnl_key = f"{key_prefix}scaled_pnl"
    reason_key = f"{key_prefix}exit_reason"
    pnl_total = sum(r[pnl_key] or 0 for r in results)
    n_missed = sum(1 for r in results if r[reason_key] == "missed_entry")
    n_unavail = sum(1 for r in results if r[reason_key] == "weex_unavailable")
    n_no_data = sum(1 for r in results if r[reason_key] == "no_data")
    n_executed = sum(1 for r in results if r[pnl_key] is not None
                     and r[reason_key] not in ("missed_entry", "weex_unavailable", "no_data"))
    wins = [r for r in results if (r[pnl_key] or 0) > 0]
    losses = [r for r in results if (r[pnl_key] or 0) < 0]
    win_rate = len(wins) / max(1, n_executed)
    gross_win = sum(r[pnl_key] for r in wins)
    gross_loss = -sum(r[pnl_key] for r in losses)
    pf = (gross_win / gross_loss) if gross_loss > 0 else None

    # Max drawdown on chronological equity curve
    sorted_r = sorted(results, key=lambda r: r["date"])
    equity = ACCOUNT_USD
    peak = ACCOUNT_USD
    max_dd_pct = 0.0
    for r in sorted_r:
        equity += r[pnl_key] or 0
        peak = max(peak, equity)
        dd = (peak - equity) / peak if peak > 0 else 0
        max_dd_pct = max(max_dd_pct, dd)

    return {
        "total_pnl": round(pnl_total, 2),
        "return_pct": round(pnl_total / ACCOUNT_USD * 100, 2),
        "n_executed": n_executed,
        "n_missed_entries": n_missed,
        "n_unavailable": n_unavail,
        "n_no_data": n_no_data,
        "win_rate": round(win_rate, 4),
        "profit_factor": round(pf, 3) if pf is not None else None,
        "max_drawdown_pct": round(max_dd_pct * 100, 2),
    }


def main():
    if not TRADES_PATH.exists():
        print(f"ERROR: {TRADES_PATH} not found", file=sys.stderr)
        sys.exit(1)
    data = json.loads(TRADES_PATH.read_text())
    all_trades = data["trades"]
    trades = [t for t in all_trades if t.get("scaled_pnl") is not None]
    print(f"Loaded {len(all_trades)} trades, {len(trades)} sized")

    by_symbol = defaultdict(list)
    for t in trades:
        by_symbol[t["symbol"]].append(t)
    print(f"Symbols: {len(by_symbol)}")

    results = []
    for sym in sorted(by_symbol.keys()):
        sym_trades = by_symbol[sym]
        print(f"  {sym}: {len(sym_trades)} trades")

        for t in sym_trades:
            start_ms, end_ms = compute_trade_window(t)

            # ── WEEX walk ───────────────────────────────────────────────────
            if sym in WEEX_UNAVAILABLE:
                wr = {
                    "weex_entry_price": None,
                    "weex_exit_price": None,
                    "weex_exit_reason": "weex_unavailable",
                    "weex_pnl_pct": None,
                    "weex_scaled_pnl": None,
                    "notes": ["symbol_not_on_weex"],
                }
            else:
                try:
                    candles_w = get_candles(sym, start_ms, end_ms, venue="weex")
                except Exception as e:
                    print(f"    trade {t['msg_id']} WEEX fetch err: {e}", file=sys.stderr)
                    candles_w = []
                wr_raw = simulate_trade(t, candles_w)
                wr = {f"weex_{k.replace('weex_', '')}" if k.startswith("weex_") else f"weex_{k}": v
                      for k, v in wr_raw.items()}
                # The simulate_trade already returns weex_* keys
                wr = {k: wr_raw[k] for k in (
                    "weex_entry_price", "weex_exit_price", "weex_exit_reason",
                    "weex_pnl_pct", "weex_scaled_pnl", "notes",
                )}

            # ── Apples-to-apples Binance walk ────────────────────────────────
            # Mirrors the original sim's `get_klines`: try Binance fapi first,
            # fall back to WEEX historyCandles for symbols not (yet) on Binance.
            try:
                candles_b = get_candles(sym, start_ms, end_ms, venue="binance")
            except Exception as e:
                print(f"    trade {t['msg_id']} Binance fetch err: {e}", file=sys.stderr)
                candles_b = []
            # Cover gap: if Binance returned less than half the expected minutes,
            # the symbol likely wasn't listed yet — fall back to WEEX prices.
            expected = max(1, (end_ms - start_ms) // 60_000)
            if len(candles_b) < expected * 0.5 and sym not in WEEX_UNAVAILABLE:
                try:
                    candles_b = get_candles(sym, start_ms, end_ms, venue="weex")
                except Exception:
                    pass
            br_raw = simulate_trade(t, candles_b)
            br = {
                "apples_binance_entry_price": br_raw["weex_entry_price"],
                "apples_binance_exit_price": br_raw["weex_exit_price"],
                "apples_binance_exit_reason": br_raw["weex_exit_reason"],
                "apples_binance_pnl_pct": br_raw["weex_pnl_pct"],
                "apples_binance_scaled_pnl": br_raw["weex_scaled_pnl"],
            }

            results.append({**t, **wr, **br})
        print(f"    done.")

    # ── Aggregate ──────────────────────────────────────────────────────────
    weex_stats = aggregate(results, "weex_")
    apples_stats = aggregate(results, "apples_binance_")

    pnl_json = sum(t["scaled_pnl"] for t in trades)

    # Per-symbol delta (apples-to-apples: same simulator both venues)
    sym_deltas = defaultdict(lambda: {"apples_binance": 0.0, "weex": 0.0, "n": 0,
                                       "json_binance": 0.0})
    for r in results:
        s = r["symbol"]
        sym_deltas[s]["apples_binance"] += r["apples_binance_scaled_pnl"] or 0
        sym_deltas[s]["weex"] += r["weex_scaled_pnl"] or 0
        sym_deltas[s]["json_binance"] += r["scaled_pnl"] or 0
        sym_deltas[s]["n"] += 1
    deltas = [(s, d["apples_binance"], d["weex"], d["weex"] - d["apples_binance"],
               d["json_binance"], d["n"])
              for s, d in sym_deltas.items()]
    deltas.sort(key=lambda x: abs(x[3]), reverse=True)

    # Missed-entry diagnostic: trades whose limit entry would not have filled
    # on WEEX within 100 minutes of signal (cost basis still used = trade.entry)
    likely_unfilled_weex = [
        r for r in results
        if r.get("notes") and "likely_unfilled_in_100min_window" in r["notes"]
    ]
    no_data_trades = [r for r in results if r["weex_exit_reason"] == "no_data"]

    summary = {
        "json_baseline": {
            "total_pnl": round(pnl_json, 2),
            "return_pct": round(pnl_json / ACCOUNT_USD * 100, 2),
            "note": "Original Binance replay output from trades_llm_2026-05-26.json. "
                    "Generated with an older resolve_exits; methodology drift vs current walker.",
        },
        "apples_binance": {
            **apples_stats,
            "note": "Recomputed Binance baseline with the SAME walker used on WEEX.",
        },
        "weex": {
            **weex_stats,
            "note": "Same walker applied to WEEX historyCandles prices.",
        },
        "venue_delta_apples": {
            "pnl_delta_usd": round(weex_stats["total_pnl"] - apples_stats["total_pnl"], 2),
            "return_delta_pct": round(weex_stats["return_pct"] - apples_stats["return_pct"], 2),
        },
        "likely_unfilled_on_weex": [
            {"msg_id": r["msg_id"], "symbol": r["symbol"], "date": r["date"],
             "entry": r["entry"], "direction": r["direction"]}
            for r in likely_unfilled_weex
        ],
        "no_data_trades": [
            {"msg_id": r["msg_id"], "symbol": r["symbol"], "date": r["date"]}
            for r in no_data_trades
        ],
        "top_symbol_deltas": [
            {"symbol": s, "apples_binance": round(b, 2), "weex": round(w, 2),
             "delta": round(d, 2), "json_binance": round(jb, 2), "n_trades": n}
            for s, b, w, d, jb, n in deltas[:10]
        ],
    }

    OUTPUT_PATH.write_text(json.dumps({
        "summary": summary,
        "trades": results,
    }, indent=2, default=str))

    # ── Print summary ──────────────────────────────────────────────────────
    print()
    print("=" * 70)
    print("WEEX REPLAY SUMMARY")
    print("=" * 70)
    print(f"{'JSON baseline (orig sim)':30s}  ${pnl_json:+8.2f}  ({pnl_json/ACCOUNT_USD*100:+6.2f}%)")
    print(f"{'Apples Binance (this walker)':30s}  ${apples_stats['total_pnl']:+8.2f}  "
          f"({apples_stats['return_pct']:+6.2f}%)")
    print(f"{'WEEX (this walker)':30s}  ${weex_stats['total_pnl']:+8.2f}  "
          f"({weex_stats['return_pct']:+6.2f}%)")
    print(f"{'Venue delta (WEEX-Binance)':30s}  ${weex_stats['total_pnl']-apples_stats['total_pnl']:+8.2f}  "
          f"({weex_stats['return_pct']-apples_stats['return_pct']:+6.2f}pp)")
    print()
    print(f"{'Metric':30s}  {'Apples-Binance':>15s}  {'WEEX':>15s}")
    print(f"{'-'*30}  {'-'*15}  {'-'*15}")
    print(f"{'Trades executed':30s}  {apples_stats['n_executed']:>15d}  {weex_stats['n_executed']:>15d}")
    print(f"{'Missed entries':30s}  {apples_stats['n_missed_entries']:>15d}  {weex_stats['n_missed_entries']:>15d}")
    print(f"{'Symbols not listed':30s}  {apples_stats['n_unavailable']:>15d}  {weex_stats['n_unavailable']:>15d}")
    print(f"{'Win rate':30s}  {apples_stats['win_rate']*100:>14.1f}%  {weex_stats['win_rate']*100:>14.1f}%")
    pf_a = f"{apples_stats['profit_factor']:.2f}" if apples_stats['profit_factor'] else "n/a"
    pf_w = f"{weex_stats['profit_factor']:.2f}" if weex_stats['profit_factor'] else "n/a"
    print(f"{'Profit factor':30s}  {pf_a:>15s}  {pf_w:>15s}")
    print(f"{'Max drawdown':30s}  {apples_stats['max_drawdown_pct']:>14.2f}%  {weex_stats['max_drawdown_pct']:>14.2f}%")
    print()
    print("Top 10 |WEEX - apples-Binance| symbol deltas:")
    print(f"  {'sym':10s}  {'apples-BN':>10s}  {'WEEX':>10s}  {'delta':>10s}  {'json-BN':>10s}  n")
    for s, b, w, d, jb, n in deltas[:10]:
        print(f"  {s:10s}  ${b:+9.2f}  ${w:+9.2f}  ${d:+9.2f}  ${jb:+9.2f}  {n}")
    print()
    print(f"Likely unfilled on WEEX (cost basis used anyway): {len(likely_unfilled_weex)} trades")
    print(f"No-data trades on WEEX:  {len(no_data_trades)} trades")
    print()
    print(f"Wrote {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
