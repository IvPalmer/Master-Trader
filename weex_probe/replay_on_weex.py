"""Re-simulate the Insiders Scalp 83-day replay using WEEX historical prices.

Reads `docs/insiders-signals/replay/trades_llm_2026-05-26.json` (Dennis's
147 sized trades over 83 days, +$763.96 / +76.4% baseline on Binance) and
walks each trade's WEEX 1m candles to compute what would have actually
happened on Dennis's venue.

Important methodology note (apples-to-apples):
  The trade JSON's `scaled_pnl` values were generated by an earlier version
  of `resolve_exits` whose behavior we cannot fully reproduce. Comparing our
  WEEX walk to those numbers conflates two effects: (1) the venue-price
  difference and (2) the resolve_exits methodology drift.

  To isolate effect (1), we ALSO recompute the Binance baseline with the
  same walker we use on WEEX. That gives a clean delta:
    apples_binance_total  ←  same walker, Binance prices
    apples_weex_total     ←  same walker, WEEX prices
  Both are reported alongside the JSON's `binance_baseline_pnl`.

Walker semantics (mirrors `ft_userdata/insiders_bridge/_local/weex.py`):
  - Uses ONLY the original SL/TP from the trade payload (no move_sl /
    close_partial / increase events apply, consistent with the Binance sim).
  - If a close_full event exists, the walk window ends at that event's minute.
  - If neither SL nor TP fires in the walk, exits at last candle close
    (reason='manual' if there was a close_full, else 'open').
  - Walk capped at BINANCE_KLINE_LIMIT minutes past entry (replicates the
    original sim's get_klines limit=1000 behavior).
  - Market entry: next-minute open after signal timestamp.
  - Limit entry: first minute whose low<=entry (long) or high>=entry (short).
  - Conservative TP/SL ordering: TP checked first within candle (matches sim).

WEEX symbol mapping (probed against api-contract.weex.com):
  - Most coins:   cmt_{lower}usdt           e.g. cmt_btcusdt
  - PEPE / 1000PEPE: cmt_1000pepeusdt        same scale as `1000PEPE` in trades
  - XTIU: NOT LISTED — trades marked weex_unavailable

Output:
  weex_probe/replay_on_weex_output.json — per-trade + summary

Read-only on docs/insiders-signals/replay/trades_llm_2026-05-26.json.
"""
from __future__ import annotations

import json
import os
import sys
import time
from collections import defaultdict
from datetime import datetime, timedelta, timezone
from pathlib import Path
from typing import Optional

import requests

HERE = Path(__file__).parent
ROOT = HERE.parent
TRADES_PATH = ROOT / "docs/insiders-signals/replay/trades_llm_2026-05-26.json"
CACHE_DIR = HERE / "historical_klines"
OUTPUT_PATH = HERE / "replay_on_weex_output.json"

WEEX_HISTORY_URL = "https://api-contract.weex.com/capi/v2/market/historyCandles"
WEEX_KLINES_MAX = 100         # endpoint cap
BINANCE_FAPI_URL = "https://fapi.binance.com/fapi/v1/klines"
BINANCE_KLINES_MAX = 1500     # endpoint cap (we use 1000 for sim parity)
RATE_LIMIT_HZ = 12            # stay under WEEX 20 req/s ceiling

ACCOUNT_USD = 1000.0          # baseline notional
RISK_PER_TRADE_USD = 10.0     # per the original sim
MARGIN_PER_TRADE_USD = 50.0   # per the original sim
HARD_TIMEOUT_DAYS = 14        # cap how long we'll walk an unclosed trade
NEXT_MINUTE_BUFFER_MS = 60_000

# The original Binance simulator (ft_userdata/insiders_bridge/_local/weex.py)
# calls `get_klines(..., limit=1000)`, capping each trade's candle walk at the
# first 1000 minutes (~16.6h) after the entry signal. We replicate that cap so
# the comparison isolates the venue-price effect, not the simulator's bug.
# Set to None to walk the full window (apples-to-oranges to original +$763.96).
BINANCE_KLINE_LIMIT = 1000


# Symbols Dennis trades that are NOT on WEEX (confirmed via probe)
WEEX_UNAVAILABLE = {"XTIU"}

# Symbols that need a 1000-prefix on WEEX (price × 0.001 to match Binance scale)
WEEX_1000_PREFIX = {"PEPE"}


# ── Cache + fetch ───────────────────────────────────────────────────────────

def weex_symbol(symbol: str) -> Optional[str]:
    """Map a trade symbol (e.g. 'BTC', '1000PEPE') to WEEX historyCandles."""
    s = symbol.upper()
    if s in WEEX_UNAVAILABLE:
        return None
    if s == "1000PEPE":
        return "cmt_1000pepeusdt"
    if s == "PEPE":  # shouldn't occur but defensive
        return "cmt_1000pepeusdt"
    return f"cmt_{s.lower()}usdt"


def price_scale(symbol: str) -> float:
    """Multiplier to convert WEEX-quoted price into trade-quoted price.

    For 1000PEPE: WEEX shows price for 1000 tokens; trade is denominated in
    1000-token units (same!) so scale = 1.0. The trade JSON has symbol
    `1000PEPE` and prices already in the 1000-token quote, so no rescale.
    """
    return 1.0


_last_request_ts = [0.0]


def _rate_limit():
    min_gap = 1.0 / RATE_LIMIT_HZ
    elapsed = time.monotonic() - _last_request_ts[0]
    if elapsed < min_gap:
        time.sleep(min_gap - elapsed)
    _last_request_ts[0] = time.monotonic()


def fetch_weex_candles(symbol: str, start_ms: int, end_ms: int) -> list[dict]:
    """Fetch 1m WEEX candles in [start_ms, end_ms]. Returns oldest-first.

    Paginates 100-at-a-time. Returns [] on error (logged to stderr).
    """
    wex = weex_symbol(symbol)
    if wex is None:
        return []
    out = []
    cursor_end = end_ms
    seen_times = set()
    while cursor_end > start_ms:
        _rate_limit()
        try:
            r = requests.get(
                WEEX_HISTORY_URL,
                params={
                    "symbol": wex,
                    "granularity": "1m",
                    "startTime": start_ms,
                    "endTime": cursor_end,
                    "limit": WEEX_KLINES_MAX,
                    "priceType": "LAST",
                },
                timeout=15,
            )
            data = r.json()
        except Exception as e:
            print(f"  [WARN] fetch {wex} err: {e}", file=sys.stderr)
            break
        if not isinstance(data, list):
            print(f"  [WARN] fetch {wex} non-list: {data}", file=sys.stderr)
            break
        if not data:
            break
        # WEEX returns newest-first; iterate oldest to newest for cursor
        rows_sorted = sorted(data, key=lambda r: int(r[0]))
        new_rows = 0
        for row in rows_sorted:
            t = int(row[0])
            if t in seen_times:
                continue
            seen_times.add(t)
            out.append({
                "t": t,
                "o": float(row[1]),
                "h": float(row[2]),
                "l": float(row[3]),
                "c": float(row[4]),
                "v": float(row[5]),
            })
            new_rows += 1
        if new_rows == 0:
            break
        # Move cursor backward: next request ends just before the oldest we have
        oldest_t = min(int(r[0]) for r in rows_sorted)
        if oldest_t <= start_ms:
            break
        cursor_end = oldest_t - 1
    out.sort(key=lambda c: c["t"])
    return out


def cache_path(symbol: str, venue: str = "weex") -> Path:
    suffix = "" if venue == "weex" else f".{venue}"
    return CACHE_DIR / f"{symbol.upper()}{suffix}.jsonl"


def load_cache(symbol: str, venue: str = "weex") -> dict[int, dict]:
    p = cache_path(symbol, venue)
    if not p.exists():
        return {}
    out = {}
    for line in p.read_text().splitlines():
        line = line.strip()
        if not line:
            continue
        try:
            c = json.loads(line)
            out[int(c["t"])] = c
        except Exception:
            pass
    return out


def append_cache(symbol: str, candles: list[dict], venue: str = "weex") -> None:
    CACHE_DIR.mkdir(parents=True, exist_ok=True)
    p = cache_path(symbol, venue)
    with p.open("a") as f:
        for c in candles:
            f.write(json.dumps(c) + "\n")


def fetch_binance_candles(symbol: str, start_ms: int, end_ms: int) -> list[dict]:
    """Fetch 1m Binance fapi candles in [start_ms, end_ms], oldest-first.

    Paginates 1500-at-a-time. Returns [] if symbol not listed or error.
    """
    pair = symbol.upper()
    if pair == "1000PEPE":
        pair = "1000PEPEUSDT"
    elif not pair.endswith("USDT"):
        pair = f"{pair}USDT"
    out = []
    cursor = start_ms
    seen = set()
    while cursor < end_ms:
        _rate_limit()
        try:
            r = requests.get(BINANCE_FAPI_URL, params={
                "symbol": pair, "interval": "1m",
                "startTime": cursor, "endTime": end_ms,
                "limit": BINANCE_KLINES_MAX,
            }, timeout=15)
            data = r.json()
        except Exception as e:
            print(f"  [WARN] binance fetch {pair} err: {e}", file=sys.stderr)
            break
        if not isinstance(data, list) or not data:
            break
        new_rows = 0
        for row in data:
            t = int(row[0])
            if t in seen:
                continue
            seen.add(t)
            out.append({
                "t": t, "o": float(row[1]), "h": float(row[2]),
                "l": float(row[3]), "c": float(row[4]), "v": float(row[5]),
            })
            new_rows += 1
        if new_rows == 0:
            break
        cursor = int(data[-1][0]) + 60_000
    out.sort(key=lambda c: c["t"])
    return out


def get_candles(symbol: str, start_ms: int, end_ms: int, venue: str = "weex") -> list[dict]:
    """Return all 1m candles in [start_ms, end_ms] for `venue`. Caches per-symbol.

    venue: 'weex' (uses WEEX historyCandles, paginated 100/req)
           'binance' (uses Binance fapi, paginated 1500/req)
    """
    cache = load_cache(symbol, venue)
    # Identify gaps. Simple approach: if our cache covers anything in range, check
    # coverage; otherwise fetch the whole range.
    # Round to minute boundaries
    start_minute = start_ms - (start_ms % 60_000)
    end_minute = end_ms - (end_ms % 60_000)
    if end_minute < start_minute:
        return []

    expected_count = (end_minute - start_minute) // 60_000 + 1
    have = sum(1 for t in cache if start_minute <= t <= end_minute)

    # We allow up to 5% missing minutes — WEEX sometimes has no-trade gaps for
    # illiquid alt pairs. If coverage is below that, refetch the range.
    if have < expected_count * 0.95:
        if venue == "binance":
            new_candles = fetch_binance_candles(symbol, start_minute, end_minute)
        else:
            new_candles = fetch_weex_candles(symbol, start_minute, end_minute)
        if new_candles:
            fresh = [c for c in new_candles if c["t"] not in cache]
            if fresh:
                append_cache(symbol, fresh, venue)
                for c in fresh:
                    cache[c["t"]] = c
    out = [c for t, c in cache.items() if start_minute <= t <= end_minute]
    out.sort(key=lambda c: c["t"])
    return out


# ── Replay logic ────────────────────────────────────────────────────────────

def parse_dt(s: str) -> datetime:
    return datetime.fromisoformat(s.replace("Z", "+00:00"))


def ms(s: str) -> int:
    return int(parse_dt(s).timestamp() * 1000)


def compute_trade_window(trade: dict) -> tuple[int, int]:
    """Return (start_ms, end_ms) for kline fetch.

    To mirror the Binance sim, we cap the fetch window at:
      max(close_full_ts, +1000 minutes from entry, +HARD_TIMEOUT_DAYS)
    The actual walk is further capped at the BINANCE_KLINE_LIMIT in simulate_trade.
    """
    start_ms = ms(trade["date"])
    # End at first close_full event if present, else hard timeout
    close_full_ms = None
    for e in trade.get("events") or []:
        if e.get("kind") == "close_full":
            close_full_ms = ms(e["date"])
            break
    if close_full_ms is not None:
        end_ms = close_full_ms + 60_000  # +1m buffer
    else:
        end_ms = start_ms + HARD_TIMEOUT_DAYS * 24 * 3600 * 1000
    # Apply Binance kline limit cap to keep fetches small
    if BINANCE_KLINE_LIMIT is not None:
        end_ms = min(end_ms, start_ms + (BINANCE_KLINE_LIMIT + 60) * 60_000)
    return start_ms, end_ms


def find_candle(candles: list[dict], minute_ms: int) -> Optional[dict]:
    """Linear scan — fine for ≤ ~20k candles per trade window."""
    for c in candles:
        if c["t"] == minute_ms:
            return c
    return None


def find_index(candles: list[dict], minute_ms: int) -> int:
    """Binary search; returns index of first candle with t >= minute_ms."""
    lo, hi = 0, len(candles)
    while lo < hi:
        mid = (lo + hi) // 2
        if candles[mid]["t"] < minute_ms:
            lo = mid + 1
        else:
            hi = mid
    return lo


def simulate_trade(trade: dict, candles: list[dict]) -> dict:
    """Re-simulate one trade on WEEX candles.

    Mirrors the Binance sim's `resolve_exits` semantics exactly (so the
    comparison isolates the venue-price effect, not the simulator semantics):
      - Uses ONLY the original SL/TP from the trade payload.
      - Does NOT apply move_sl / close_partial / increase events (consistent
        with the Binance sim, which ignores them when walking candles).
      - The close_full event timestamp bounds the walk window — if neither SL
        nor TP fires before that timestamp, the trade exits at the last
        available candle close as "manual".
      - Otherwise walks to the end of available candles ("open").

    Entry semantics:
      - Market entry: next-minute open after signal timestamp.
      - Limit entry (long): first minute whose low <= entry price.
      - Limit entry (short): first minute whose high >= entry price.

    Returns a result dict with weex_entry_price, weex_exit_price,
    weex_exit_reason, weex_pnl_pct, weex_scaled_pnl, notes.
    """
    notes = []
    if not candles:
        return {
            "weex_entry_price": None,
            "weex_exit_price": None,
            "weex_exit_reason": "no_data",
            "weex_pnl_pct": None,
            "weex_scaled_pnl": None,
            "notes": ["no_weex_candles"],
        }

    direction = trade["direction"].upper()
    entry_price = trade.get("entry")
    if entry_price is None:
        return {
            "weex_entry_price": None,
            "weex_exit_price": None,
            "weex_exit_reason": "no_data",
            "weex_pnl_pct": None,
            "weex_scaled_pnl": None,
            "notes": ["trade_has_no_entry"],
        }
    entry_price = float(entry_price)

    entry_minute = ms(trade["date"]) - (ms(trade["date"]) % 60_000)
    start_idx = find_index(candles, entry_minute)
    if start_idx >= len(candles):
        return {
            "weex_entry_price": None,
            "weex_exit_price": None,
            "weex_exit_reason": "no_data",
            "weex_pnl_pct": None,
            "weex_scaled_pnl": None,
            "notes": ["no_candle_at_entry"],
        }

    # ── Entry fill ──────────────────────────────────────────────────────────
    # Match the Binance sim's assumption: the trade IS opened at `trade.entry`
    # regardless of whether the venue's price crossed it. The Binance sim does
    # not check entry-fill — it walks candles from signal time forward, treating
    # trade.entry as the cost basis. We mirror that for apples-to-apples.
    # We DO still mark trades whose entry is far from the venue's price as
    # likely-missed for diagnostic purposes (note added; PnL still computed).
    entry_candle = candles[start_idx]
    weex_entry_idx = start_idx

    if trade.get("market_entry"):
        # Market: next-minute open after signal
        if start_idx + 1 < len(candles):
            weex_entry = candles[start_idx + 1]["o"]
            weex_entry_idx = start_idx + 1
        else:
            weex_entry = entry_candle["c"]
            notes.append("market_entry_last_candle_close")
    else:
        # Limit: use the trade's specified entry price as cost basis.
        weex_entry = entry_price
        # Diagnostic: would this entry actually have filled in the venue's price
        # action? (Long: low<=entry within walk window; Short: high>=entry)
        would_fill = False
        for i in range(start_idx, min(start_idx + 100, len(candles))):  # 100min look-ahead
            c = candles[i]
            if direction == "LONG" and c["l"] <= entry_price:
                would_fill = True
                weex_entry_idx = i
                break
            if direction == "SHORT" and c["h"] >= entry_price:
                would_fill = True
                weex_entry_idx = i
                break
        if not would_fill:
            notes.append("likely_unfilled_in_100min_window")

    # ── Determine walk-end (matches Binance sim) ────────────────────────────
    # If channel posted a close_full, walk stops at that event's minute.
    close_full_ms = None
    for e in trade.get("events") or []:
        if e.get("kind") == "close_full":
            close_full_ms = ms(e["date"])
            break  # use the first close_full only

    end_idx = len(candles)
    if close_full_ms is not None:
        end_minute = close_full_ms - (close_full_ms % 60_000)
        ei = find_index(candles, end_minute)
        end_idx = min(ei + 1, len(candles))  # inclusive of close_full minute

    # Replicate the Binance sim's limit=1000 cap (1000 candles past entry).
    if BINANCE_KLINE_LIMIT is not None:
        end_idx = min(end_idx, weex_entry_idx + BINANCE_KLINE_LIMIT)

    # ── Walk for SL/TP ──────────────────────────────────────────────────────
    sl = trade.get("sl")
    if isinstance(sl, str):
        sl = None
    tp = trade.get("tp")
    if isinstance(tp, str):
        tp = None

    exit_price = None
    exit_reason = None

    for i in range(weex_entry_idx, end_idx):
        c = candles[i]
        high, low = c["h"], c["l"]
        if direction == "LONG":
            # Binance sim checks TP first inside its loop, then SL. Mirror.
            if tp and high >= tp:
                exit_price = tp
                exit_reason = "tp"
                break
            if sl and low <= sl:
                exit_price = sl
                exit_reason = "sl"
                break
        else:  # SHORT
            if tp and low <= tp:
                exit_price = tp
                exit_reason = "tp"
                break
            if sl and high >= sl:
                exit_price = sl
                exit_reason = "sl"
                break

    if exit_price is None:
        # Fallback exit: last candle close in walk window
        last_c = candles[end_idx - 1] if end_idx > 0 else candles[-1]
        exit_price = last_c["c"]
        if close_full_ms is not None:
            exit_reason = "manual"
        else:
            exit_reason = "open"

    # Compute PnL
    if direction == "LONG":
        pnl_pct = (exit_price - weex_entry) / weex_entry
    else:
        pnl_pct = (weex_entry - exit_price) / weex_entry

    notional = trade.get("position_notional") or 0.0
    weex_scaled_pnl = round(notional * pnl_pct, 2)

    return {
        "weex_entry_price": weex_entry,
        "weex_exit_price": exit_price,
        "weex_exit_reason": exit_reason,
        "weex_pnl_pct": pnl_pct,
        "weex_scaled_pnl": weex_scaled_pnl,
        "notes": notes,
    }


